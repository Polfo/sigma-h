    /**
     * Builds the help window.
     */
    private SigmahHelpWindow() {

        // The content.
        html = new HTML();
        html.addStyleName("help-content");

        // Main window panel.
        final ScrollPanel sp = new ScrollPanel(html);
        sp.setAlwaysShowScrollBars(false);
        sp.setWidth("100%");

        // Main window panel.
        final ContentPanel mainPanel = new ContentPanel();
        mainPanel.setHeaderVisible(false);
        mainPanel.setLayout(new FitLayout());
        mainPanel.add(sp);

        // Window.
        window = new Window();
        window.setHeading(I18N.CONSTANTS.help());
        window.setSize(450, 525);
        window.setPlain(true);
        window.setModal(true);
        window.setBlinkModal(true);
        window.setLayout(new FitLayout());
        window.add(mainPanel);
    }

    /**
     * Singleton.
     */
    private static SigmahHelpWindow instance;

    /**
     * Builds the unique instance of this class if necessary.
     */
    private static void ensureInstance() {
        if (instance == null) {
            instance = new SigmahHelpWindow();
        }
    }

    /**
     * Shows the help window.
     * 
     * @param pageId
     *            The current page id.
     */
    public static void show(PageId pageId) {

        ensureInstance();

        instance.window.hide();
        instance.html.setHTML(getContent(pageId));
        instance.window.show();
    }

